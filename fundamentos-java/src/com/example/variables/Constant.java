package com.example.variables;

/**
 * Las constantes permiten almacenar información que tiene un valor inicial,
 * dado por el programador y que nunca es modificado. Por tanto se dice que el valor
 * es Constante o Final.
 */
public class Constant {

    /**
     * Este es el método principal de una aplicación construida en Java. Este se encargará de ejecutar
     * el conjunto de instrucciones de nuestra aplicación.
     *
     * @param args Argumentos pasados por consola.
     */
    public static void main(String[] args) {

        // Constante que representa los lados de un triángulo.
        final int LADOS_TRIANGULO = 3;

        // Constante que representa al número PI.
        final double PI = 3.14159;
    }
}
