package com.example.variables;

/**
 * Las variables permiten almacenar información relevante a la ejecución
 * del programa. Esta información generalmente está sujeta a cambios,
 * por tanto decimos que es variable.
 */
public class Variable {

    /**
     * Este es el método principal de una aplicación construida en Java. Este se encargará de ejecutar
     * el conjunto de instrucciones de nuestra aplicación.
     *
     * @param args Argumentos pasados por consola.
     */
    public static void main(String[] args) {
        //Declaración de variable
        int suma;
        //Asignación de valor
        suma = 2;

        //Es posible declarar una variable y asignar su valor inmediatamente
        int resta = 2;
    }
}
